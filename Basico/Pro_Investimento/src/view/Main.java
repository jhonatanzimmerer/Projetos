    public int ExistM(String ativo, int quantidade){
        Mnp sql = new Mnp();
        List<Momento> list = new ArrayList<>();
        list = sql.BuscarM();
        for(int x=0;x<list.size();x++){
            if(list.get(x).getAtivo().equals(ativo)){
                if(list.get(x).getQuantidade() > quantidade)
                    return 1;
                else if(list.get(x).getQuantidade() == quantidade)
                    return 0;
                else
                    return -1;
            }
        }
        return -2;
    }
    
    public String ConvertDate(String data){
        String [] novo = data.split("/");
        data = novo[2] + "-" + novo[1] + "-" + novo[0];
        return data;
    }
    
    
