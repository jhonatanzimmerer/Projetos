    private void BtnimportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnimportarActionPerformed
        File file;
        Workbook wk;
        List<Operacao> list = new ArrayList<>();
        Mnp sql = new Mnp();
        
        try {
            wk = Workbook.getWorkbook(new File(Txtlocal.getText().trim()));
            
            Sheet st = wk.getSheet(0);
           
            int linhas = st.getRows();
            for(int i=1;i<linhas;i++){
                
                Cell cat = st.getCell(0,i);
                Cell cqt = st.getCell(1,i);
                Cell cpc = st.getCell(2,i);
                Cell cdt = st.getCell(3,i);
                Cell ctx = st.getCell(4,i);
                Cell cop = st.getCell(5,i);
                Cell ctp = st.getCell(6,i);
                
                
                
                Operacao op = new Operacao();
                op.setAtivo(String.valueOf(cat.getContents()));
                op.setData(ConvertDate(String.valueOf(cdt.getContents())));
                op.setOp(String.valueOf(cop.getContents()));
                op.setPreco(Float.valueOf(cpc.getContents().replace(",", ".")));
                op.setQuantidade(Integer.valueOf(cqt.getContents()));
                op.setTaxa(Float.valueOf(ctx.getContents().replace(",", ".")));
                op.setTipo(String.valueOf(ctp.getContents()));

                list.add(op);
            }
            
            try{
                String ativo;
                String data ;
                String op;
                float preco;
                int quantidade;
                float taxa;
                String tipo;
                
                for(int x=0;x<list.size();x++){
                
                    ativo = list.get(x).getAtivo();
                    data = list.get(x).getData();
                    op = list.get(x).getOp();
                    preco = list.get(x).getPreco();
                    quantidade = list.get(x).getQuantidade();
                    taxa = list.get(x).getTaxa();
                    tipo = list.get(x).getTipo();
                    
                    sql.InsertO(ativo, quantidade, preco, data, taxa, op, tipo);
                    
                    if(op.equals("C")){
                       if(ExistM(ativo, quantidade) >= -1)
                           sql.InsertM(ativo, quantidade, preco, taxa, true);
                       else if(ExistM(ativo, quantidade) == -2)
                           sql.InsertM(ativo, quantidade, preco, taxa, false);
                    }
                    else if(op.equals("V")){
                        try{
                            float precom;
                            List<Momento> listm = new ArrayList<>();
                            listm = sql.BuscarM();
                            boolean validar = false;
                            for(int y=0;y<listm.size();y++){
                                
                                if(listm.get(y).getAtivo().equals(ativo)){
                                    precom = Float.valueOf(listm.get(y).getPreco_medio());
                                    if(listm.get(y).getQuantidade()-quantidade > 0)
                                        validar = true;
                                    sql.InserV(ativo, quantidade, preco, precom, data, tipo, validar);
                                }
                            }
                            
                        } catch(Exception e){
                            JOptionPane.showMessageDialog(null, "Não tem ações suficientes para a venda");
                        }
                    }
                }
                
                
            } catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro ao importar: " + ex);
            } finally {
                JOptionPane.showMessageDialog(null, "Terminou a operação");
            }
            
            wk.close();
            
        } catch (IOException | BiffException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnimportarActionPerformed

