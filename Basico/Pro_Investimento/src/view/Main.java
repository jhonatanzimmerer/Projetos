    private void BntconfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntconfirmarActionPerformed
      
        String ativo = Txtativo.getText();
            int quantidade = Integer.valueOf(Txtquantidade.getText());
            float preco = Float.valueOf(Txtpreco.getText().replace(",", "."));
            String data = ConvertDate(Txtdata.getText());
            float taxa = Float.valueOf(Txtaxa.getText().replace(",", "."));
            String op = "";
            String tipo = "";
            Mnp sql = new Mnp();
            
            if(Rbcompra.isSelected())
                op = "C";
            else if(Rbvenda.isSelected())
                op = "V";
            if(Rbfii.isSelected())
                tipo = "F"; 
            else if(Rbacao.isSelected())
                tipo = "A";

            if(op.equals("C")){
                if(ExistM(ativo, quantidade) >= -1)
                    sql.InsertM(ativo, quantidade, preco, taxa, true);
                else if(ExistM(ativo, quantidade) == -2)
                    sql.InsertM(ativo, quantidade, preco, taxa, false);
                sql.InsertO(ativo, quantidade, preco, data, taxa, op, tipo);
            }
            else if(op.equals("V")){
                try{
                    float precom;
                    List<Momento> listm = new ArrayList<>();
                    listm = sql.BuscarM();
                    boolean validar = false;
                    
                    for(int y=0;y<listm.size();y++){
                        if(listm.get(y).getAtivo().equals(ativo)){
                            precom = Float.valueOf(listm.get(y).getPreco_medio());
                            if(listm.get(y).getQuantidade()-quantidade > 0)
                                validar = true;
                            sql.InserV(ativo, quantidade, preco, precom, data, tipo, validar);
                        }
                    }
                    sql.InsertO(ativo, quantidade, preco, data, taxa, op, tipo);
                    JOptionPane.showMessageDialog(null, "Operação realizada com sucesso!");
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Não tem ações suficientes para a venda");
                }
            }
            
            

    }//GEN-LAST:event_BntconfirmarActionPerformed

